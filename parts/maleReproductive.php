							<b>Seminal Duct / Ejaculatory Duct</b> - On each site of the prostate gland in the male reproductive system is an ejaculatory duct. Each ejaculatory duct is about two centimeters in length and is created when the seminal vesicle's duct merges with the vas deferens. At this point, these ducts travel through the prostate and, at its end, drain into the collicus seminalis's urethra. Semen flows through each ejaculatory duct during ejaculation, and travels through the penis to exit the body. One ejactulatory duct or both can become obstructed in a congenital or acquired pathological complication known as ejactulatory duct obstruction. When both ducts are obstructed, patients are likely to experience symptoms such as infertility and azoospermia or aspermia. Transurethral resection is a surgical procedure that can be used to open the ducts; its goal is to make regular ejaculation flow possible once again. Benign prostatic hyperplasia, which refers to an abnormal increase in prostate size, is another condition that can affect the ejaculatory ducts. Although this condition is correctable by surgery, an ejaculatory duct could be damaged in the process, causing retrograde ejaculation.
