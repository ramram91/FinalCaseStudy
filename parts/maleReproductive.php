						<p>
						<b> Note:</b> Other terms used to refer to the "Urinary System" include the "Renal System" and the "Genito-urinary System".<br/><br/>
							Each part of the urinary system performs important functions - both for the efficient operation of the urinary system itself, and also therefore, for the body as a whole. <br/>
							<h2>Functions of the Kidneys:</h2><br/>
							<b> Regulation of blood volume:</b><br/>
							The kidneys conserve or eliminate water from the blood, which regulates the volume of blood in the body.<br/>
							<b>Regulation of blood pressure:</b><br/>
							<b> The kidneys regulate blood pressure in 3 ways, by:-</b><br/>
							-	Adjusting the volume of blood in the body (by regulating the quantity of water in the blood - see above)<br/>
							-	Adjusting the flow of blood both into, and out of, the kidneys<br/>
							-	Via the action of the enzyme renin. The kidneys secret renin, which activates the angiotensin-aldosterone pathway. <br/><br/>
							<b> Regulation of the pH of the blood:</b><br/>
							The kidneys excrete H+ ions (hydrogen atoms that lack their single electron), into urine. At the same time, the kidneys also conserve bicarbonate ions (HCO3-), which are an important buffer of H+.<br/><br/>
							<b> Regulation of the ionic composition of blood:</b><br/>
							The kidneys also regulate the quantties in the blood of the ions (charged particles) of several important substances. Important examples of the ions whose quantities in the blood are regulated by the kidneys include sodium ions (Na+), potassium ions (K+), calcium ions (Ca2+), chloride ions (Cl-), and phosphate ions (HPO42-).<br/><br/>
							<b> Production of Red blood cells:</b><br/>
							The kidneys contribute to the production of red blood cells by releasing the hormone erythropoietin - which stimulates erythropoiesis (the production of red blood cells). <br/><br/>
							<b> Synthesis of Vitamin D:</b><br/>
							The kidneys (as well as the skin and the liver) synthesize calcitrol - which is the active form of vitamin D. <br/><br/>
							<b> Excretion of waste products and foreign substances:</b><br/>
							The kidneys help to excrete waste products and foreign substance from the body by forming urine (for release from the body).
							<b> Examples of waste products </b>from metabolic reactions within the body include ammonia (from the breakdown of amino acids), bilirubin (from the breakdown of haemoglobin), and creatinine (from the breakdown of creatine phosphate in muscle fibres). Examples of foreign substances that may also be exceted in urine include pharmaceutical drugs and environmental toxins.<br/><br/>
							<b> Functions of the Ureters:</b><br/>
							There are two ureters, one leading from each kidney to the urinary bladder. Each of these transports urine from the renal pelvis of the kidney to which it is attached, to the bladder (see diagram on the page about components of the urinary system).<br/><br/>
							Both of the ureters pass beneath the urinary bladder, which results in the bladder compressing the ureters and hence preventing back-flow of urine when pressure in the bladder is high during urination. This prevention of back-flow is important because when it is not operating correctly cystitis, which is inflammation of the ureter / urinary bladder, may develop into a kidney infection. <br/><br/>
							<b> Functions of the Bladder:</b><br/>
							The purpose of the urinary bladder is to store urine prior to elimination of the urine from the body.
							The bladder also expels urine into the urethra by a process called micturition (also known as urination). Micturition involves the actions of both voluntary and involuntary muscles. Lack of voluntary control over this process is referred to as incontinence.<br/><br/>
							<b> Functions of the Urethra:</b><br/>
							The urethra is the passageway through which urine is discharged from the body.
							In males the urethra also serves as the duct through which semen is ejaculated (see the section about reproduction for further detail about this function).
