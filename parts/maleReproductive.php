							<b>Testicles / Testis</b> form part of the male reproductive system, and is the gland where sperm and testosterone are produced. The testes are contained in the scrotum and are composed of dense connective tissue containing around three hundred internal compartments called lobules. Each of these lobules contains a number of highly coiled tiny tubules where the sperm is produced. Testosterone is produced in cells located in between the lobules. Sperm travels through the testis from the tubules, through a network of ducts called the rete testis, to the epididimus. One of the most common complications that can occur with the testes is an undescended testis, which can occur in around three percent of all boys and around thirty percent of boys born prematurely. This occurs when the testes which form in the abdomen fail to descend to the scrotum, as they normally should, when puberty begins. This can be addressed by either waiting around three to six months after the initial examination for the testis to descend, or with a surgical procedure called an Orchidopexy, which involves artificially moving the testis concerned to the scrotum. The procedure is around 95 percent successful.
