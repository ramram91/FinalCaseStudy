						<b>Male Urethra </b>connects the urinary bladder to the penis, and it functions as an exit from the body. Once the bladder becomes full, urine flows through the urethra and leaves the body at the meatus located at tip of the penis. The urethra is more than just a urinary duct; it also serves as a conduit for semen and sperm during sexual acts. While urine and semen travel through the urethra, the tube is not solely responsible for moving fluids. The urethral sphincter controls voluntary and involuntary urination. This group of muscles includes both the internal and external sphincter muscles of the urethra. The bulbospongiosus muscle of the penis plays an active role in ejaculation. The male urethra is comprised of four main parts. The preprostatic urethra runs in front of the prostate, while the prostatic urethra courses through that gland. The membranous urethra travels through the external urethral sphincter, while the spongy urethra travels the length of the penis and terminates at the meatus at the tip of the sexual organ.
