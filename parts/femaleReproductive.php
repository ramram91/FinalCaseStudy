						<p>
							<b> Corpus Luteum </b>which means yellow body in Latin, is what is left of the follicle after a woman ovulates. During the follicular phase of a woman’s cycle, several follicles develop under the influence of FSH (follicle stimulating hormone). Each follicle contains an egg. In a typical cycle only one egg will become mature enough for ovulation. When a woman ovulates the egg will burst from the follicle. Then what is left of the follicle will become the corpus luteum. The luteal phase, named after the corpus luteum, is the second half of a woman’s menstrual cycle. The luteal phase begins after ovulation and continues until menstruation occurs. <br/><br/>
							<h2> What does the corpus luteum do?</h2><br/>
							The corpus luteum produces progesterone. Progesterone makes the lining of the uterus thick for implantation and is necessary to sustain a healthy pregnancy. The corpus luteum produces progesterone until the placenta begins to take over progesterone production around ten weeks gestation. <br/>
							<h2> How long does the corpus luteum survive?</h2><br/>
							After a woman ovulates, the corpus luteum only lasts for about 12-14 days unless it begins receiving HCG (human chorionic gonadotropin) from a developing embryo. If the egg is not fertilized, the corpus luteum dies and progesterone production stops. When progesterone levels drop, the uterus lining stops thickening and is consequently shed during menstruation.
							If the egg is fertilized, the corpus luteum will begin receiving HCG from the embryo. HCG tells the corpus luteum to keep producing progesterone. The corpus luteum lasts for about ten weeks after ovulation. After ten weeks the placenta takes over progesterone production through the end of pregnancy.<br/>
							<h2>What is corpus luteum deficiency? </h2><br/>
							Corpus luteum deficiency, also known as corpus luteum defect, means that the corpus luteum does not produce enough progesterone to allow a pregnancy to develop. Without sufficient progesterone levels, the lining of the uterus will begin to shed. If a woman is pregnant and has low progesterone levels this may result in miscarriage.
