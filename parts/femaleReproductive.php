						<p>
							<b>Vagina</b> is an elastic, muscular canal with a soft, flexible lining that provides lubrication and sensation. The vagina connects the uterus to the outside world. The vulva and labia form the entrance, and the cervix of the uterus protrudes into the vagina, forming the interior end.
							The vagina receives the penis during sexual intercourse and also serves as a conduit for menstrual flow from the uterus. During childbirth, the baby passes through the vagina (birth canal).
							The hymen is a thin membrane of tissue that surrounds and narrows the vaginal opening. It may be torn or ruptured by sexual activity or by exercise. <br/><br/>
							<h2>Vagina Conditions:</h2><br/>
							•	<b>Vaginitis:</b> Inflammation of the vagina, commonly from a yeast infection or bacterial overgrowth. Itching, discharge, and change of odor are typical symptoms. Vaginitis is treated with antibiotics or antifungal medication. <br/>
							•	<b>Vaginismus:</b> Involuntary spasm of the vaginal muscles during sexual intercourse. Emotional distress about sex, or medical conditions, can be responsible. It is usually treated with exercises, medication. or both. <br/>
							•	<b>Genital warts:</b> Genital warts may affect the vulva,vagina, and cervix. Treatments can remove vaginal warts, which are caused by human papillomavirus (HPV). <br/>
							•	<b>Trichomoniasis:</b> Infection of the vagina by a microscopic parasite called trichomonas. Trichomoniasis is transmitted by sex and is easily curable. <br/>
							•	<b>Bacterial vaginosis (BV):</b> A disruption in the balance of healthy bacteria in the vagina, often causing odor and discharge. Douching, or sex with a new partner can cause BV. BV is treated with antibiotics. <br/>
							•	<b>Herpes simplex virus (HSV):</b> The herpes virus can infect the vulva, vagina, and cervix, causing small, painful, recurring blisters and ulcers. Having no noticeable symptoms is also common. The virus is transmitted sexually. It can be treated, but not cured. <br/>
							•	<b>Gonorrhea:</b> This sexually transmitted bacterial infection most often infects the cervix. Half the time, there are no symptoms, but vaginal discharge and itching may occur. It can cause pelvic inflammatory disease and infertility. It is treated with antibiotics. <br/>
							•	<b>Chlamydia:</b> The bacterium Chlamydia trachomatis causes this sexually transmitted infection. Only half of women will have symptoms, which may include vaginal discharge or pain in the vagina or abdomen. It can cause pelvic inflammatory disease and infertility. Chlamydia is treated with antibiotics. <br/>
							•	<b>Vaginal cancer:</b> Cancer of the vagina is extremely rare. Abnormal vaginal bleeding or discharge are symptoms. <br/>
							•	<b>Vaginal prolapse:</b> Due to weakened pelvic muscles (usually from childbirth), the rectum, uterus, or bladder pushes on the vagina. In severe cases, the vagina protrudes out of the body. <br/><br/>
							 
							<h2>Vagina Tests:</h2><br/>
							•	<b>Pelvic examination:</b> Using a speculum, a doctor can examine the vulva, vagina, and cervix. The strength of the pelvic muscles can also be tested. <br/>
							•	<b>Papanicolaou smear (Pap smear):</b> During a pelvic exam, the examiner swabs the cervix and vagina. Pap smears screen for cervical or vaginal cancer. <br/>
							•	<b>Bacterial culture:</b> A swab of the cervix and vagina duriing a pelvic exam may be cultured in a lab. This can identify bacterial and viral infections. <br/>
							•	<b>Colposcopy:</b> A microscope is used during a pelvic exam to examine closely the vulva, vagina, and cervix. Colposcopy can help identify cancer or other problems. <br/>
							•	<b>Vaginal biopsy:</b> In the rare case of a suspicious growth in the vagina, a small piece of tissue (biopsy) may be sent to check for cancer. <br/>

