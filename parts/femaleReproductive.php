						<p>
							The uterus, also commonly known as the womb, is a hollow muscular organ of the female reproductive system that is responsible for the development of the embryo and fetus during pregnancy. An incredibly distensible organ, the uterus can expand during pregnancy from around the size of a closed fist to become large enough to hold a full term baby. It is also an incredibly strong organ, able to contract forcefully to propel a full term baby out of the body during childbirth. <br/><br/>
							The uterus is approximately the shape and size of a pear and sits in an inverted position within the pelvic cavity of the torso. It is located along the body’s midline posterior to the urinary bladder and anterior to the rectum. The narrow inferior region of the uterus, known as the cervix, connects the uterus to the vagina below it and acts as a sphincter muscle to control the flow of material into and out of the uterus. <br/><br/>
							The body (or corpus) of the uterus is the wider region of the uterus superior to the cervix. The body is an open and hollow region where the fertilized egg, or zygote, implants itself and develops during pregnancy. The walls of the body are much thicker than those of the cervix as they provide for the protection and support of the developing fetus and contain the muscles that propel the fetus out of the mother’s body during childbirth. <br/><br/>
							Superior to the body is a domed region known as the fundus of the uterus. The fallopian tubes extend laterally from the corners of the fundus. <br/><br/>
							<b>Three distinct tissue layers make up the walls of the uterus: </b><br/>
							•	The perimetrium is the outermost layer that forms the external skin of the uterus. It is a serous membrane continuous with the peritoneum that covers the major organs of the abdominopelvic cavity. The perimetrium protects the uterus from friction by forming a smooth layer of simple squamous epithelium along its surface and by secreting watery serous fluid to lubricate its surface. <br/>
							•	Deep to the perimetrium layer, the myometrium forms the middle layer of the uterus and contains many layers of visceral muscle tissue. During pregnancy the myometrium allows the uterus to expand and then contracts the uterus during childbirth. <br/>
							•	Inside the myometrium is the endometrium layer that borders the hollow lumen of the uterus. The endometrium is made of simple columnar epithelial tissue with many associated exocrine glands and a highly vascular connective tissue that provides support to the developing embryo and fetus during pregnancy. <br/>
							Around the time of ovulation the uterus builds a thick layer of vascular endometrial tissue in preparation to receive a zygote, or fertilized egg cell. If the egg cell does not become fertilized by the time it reaches the uterus, it will pass through the uterus and trigger the blood vessels of the endometrium to atrophy and the uterine lining to be shed. The shedding of the egg cell and uterine lining is known as menstruation and occurs approximately every 28 days for most women. <br/><br/>
							In the case of successful fertilization of the ova, a zygote will implant itself into the endometrial lining, where it begins to develop over many weeks into an embryo and eventually a fetus. As the embryo develops into a fetus, it triggers changes within the endometrium that lead to the formation of the placenta. The placenta provides the developing fetus with vital nutrients and oxygen from the mother’s blood, while transferring carbon dioxide and metabolic waste products to the mother’s blood for disposal. <br/><br/>
							At the end of pregnancy, the uterus plays a critical role in the process of childbirth. Prior to delivery, hormones trigger waves of smooth muscle contraction in the myometrium that slowly increase in strength and frequency. At the same time, the smooth muscle tissue of the cervix begins to efface, or thin, and dilate from less than a centimeter in diameter to around ten centimeters at full dilation. Once the cervix is fully dilated, the uterine contractions drastically increase in intensity and duration until the fetus is pushed out of the uterus, through the vagina, and out of the mother’s body.
