							<b>Wall of Uterus</b> has a thin inner layer called the endometrium. In the absence of pregnancy, this layer is largely shed each month when menstrual flow begins. The uterine wall also has a thick muscular layer called the myometrium. When a woman becomes pregnant, the embryo implants into the inner surface of the endometrium about 5 days after conception. In the earliest stages, the fetus receives its nutrition from glands in the endometrium, which secretes nutrient-rich fluid, and receives oxygen from nearby maternal blood vessels. <br/> <br/>
							As the growing embryo enlarges, it becomes surrounded by the uterine endometrium. Cells in the fetal membrane called the chorion form cords of cells, called villi, that grow into the part of the endometrium beneath the fetus, eventually forming the fetal part of the placenta. In response, maternal blood vessels and groups of cells in the endometrium that are near these fetal villi grow and expand, developing into the maternal part of the placenta.
