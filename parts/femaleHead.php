						<br/>
							Also called the oral cavity, the mouth is the first part of thegastrointestinal tract (or alimentary canal). The boundaries of the mouth are formed by the lips, cheeks, floor of the mouth, and palate. The mouth contains the teeth and tongue and receives secretions from thesalivary glands. It performs three main functions, which have to do withdigestion, breathing, and speech.<br/><br/>
							<h1>Functions of the mouth</h1>
							<b>First</b>, the mouth is the place where food is taken in and where digestion begins (see digestive system). The mouth is adapted to receive food byingestion, break it into small particles by mastication, and mix it withsaliva. The digestive functions of the mouth include:<br/>
							•	Chewing, grinding, and mixing of food<br/>
							•	Formation of a bolus<br/>
							•	Initiation of digestive processes<br/>
							•	Swallowing<br/>
							•	Taste<br/><br/>
							<b>Second</b>, the mouth is a passageway between the pharynx (the cavity connecting the nose, mouth, and larynx) and the outside of the body. It can thus be used for breathing when the nose is inadequate, as happens, for instance, during strenuous exercise. <br/><br/>
							<b>Third</b>, the mouth plays a vital part in speech (see voice), because alterations in the shape of the mouth and the lips modify the sounds that are made by the vocal folds (vocal cords) in such a way that they become recognizable as syllables.
							Structure of the mouth
							The mouth, like many organs in the human body, is a hollow cavity. The part in front of the teeth is called the vestibule, while the part behind is the mouth itself. The floor of the mouth is formed from sheets of muscle tissue which are attached to the inner surface of the jawbone, ormandible. The side walls are formed by the cheeks, which are sufficiently flexible to allow the mouth to open and close. 
							The roof of the mouth is formed by the palate, a thin sheet of tissue which separates the mouth from the nasal cavities above. At the back, the cavity of the mouth joins up with the pharynx, while at the front it communicates with the outside through the lips. 
							Except for the teeth, the whole of the inner surface of the mouth is lined by mucous membrane. At the back, the membrane goes on to line the gastrointestinal tract, and at the front it is folded over to form the lips. <br/><br/>

							<b>Lips and cheeks</b> - The lips and cheeks help hold food in the mouth and keep it in place for chewing. They are also used in the formation of words for speech. The lips contain numerous sensory receptors that are useful for judging the temperature and texture of foods. <br/><br/>
							<b>Muscles of the mouth</b> - The various parts of the mouth have to make numerous finely controlled movements in order that we may eat and speak. These movements are all brought about by the many muscles which lie under the mucous membrane, and which are attached to the skeleton, often some distance from the mouth.<br/><br/>
							<b>Palate</b> - The palate forms the roof of the mouth and separates the mouth from the nasal cavity. The palate consists of two quite different parts. The anterior (front) portion, the hard palate, is supported by bone. The posterior (back) portion, the soft palate, is skeletal muscle andconnective tissue. Posteriorly, the soft palate ends in a projection called the uvula. During swallowing, the soft palate and uvula move upward to direct food away from the nasal cavity and into the oropharynx. <br/><br/>
							<b>Salivary glands</b> - The salivary glands are small glands, found in many parts of the mouth, which produce saliva. All of them lie beneath the mucous membrane. The largest salivary glands are the parotid glands which lie, one on each side, just in front of the ears. Other large ones are the submandibular glands, in the floor of the mouth, and the sublingual glands, underneath the tongue. Saliva moistens the food that we eat, which makes swallowing easier; it also helps in the digestive process, for it contains the enzyme amylase, which breaks down thestarch in food. <br/><br/>
							<b>Tongue</b> - The tongue, which is made up of muscle fibers, is attached to the back part of the floor of the mouth. When it is not in use, it lies between the teeth and the lower jaw. Its most important tasks are to move the food about in the mouth during chewing and to assist in the making of sounds during speech. On the upper surface of the tongue are a large number of papillae that provide friction and contain the taste buds. <br/><br/>
							<b>Teeth</b> - In the human, a complete set of deciduous (primary) teeth contains 20 teeth. There are 32 teeth in a complete permanent (secondary) set. The shape of each tooth type corresponds to the way it handles food. At the front are eight chisel-shaped cutting teeth, or incisors. Behind these are the four canine teeth, and behind these are eight premolars and 12 molars. <br/><br/>
							<b>Tonsils</b> - At the back of the mouth are two thin folds of tissue on each side which run from the soft palate above to the root of the tongue below. These folds are called the pillars of the fauces. There is an anterior (front) and a posterior (back) pillar on each side, and between these pillars lie the tonsils. The tonsils are two small glands made of lymphatic tissue. <br/><br/>
							
