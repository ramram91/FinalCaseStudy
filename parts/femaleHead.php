						<p>
							<b>Aqueous Humor</b> - The aqueous humor is a watery substance that fills the eye. It is split into two chambers. The anterior chamber is located in front of the iris, and the posterior chamber is directly behind it. These layers allow the eye to maintain its shape. This liquid is drained through the Schlemm canal so that any buildup in the eye can be removed. If the patient's aqueous humor is not draining properly, they can develop glaucoma.<br/><br/>
							<b>Choroid</b> – The choroid lies between the retina and the sclera, which provides blood supply to the eye. Just like any other portion of the body, the blood supply gives nutrition to the various parts of the eye.<br/><br/>
							<b>Ciliary Body</b> – Ciliary body is a ring-shaped tissue which holds and controls the movement of the eye lens, and thus, it helps to control the shape of the lens.<br/>
							<b>Conjunctiva Glands</b> - These are layers of mucus which help keep the outside of the eye moist. If the eye dries out it can become itchy and painful. It can also become more susceptible to damage or infection. If the conjunctiva glands become infected the patient will develop "pink eye."<br/><br/>				
							<b>Cornea</b> - The cornea is the outer covering of the eye. This dome-shaped layer protects your eye from elements that could cause damage to the inner parts of the eye. There are several layers of the cornea, creating a tough layer that provides additional protection. These layers regenerate very quickly, helping the eye to eliminate damage more easily. The cornea also allows the eye to properly focus on light more effectively. Those who are having trouble focusing their eyes properly can have their corneas surgically reshaped to eliminate this problem.<br/><br/>
							<b>Iris</b> - The iris is the area of the eye that contains the pigment which gives the eye its color. This area surrounds the pupil, and uses the dilator pupillae muscles to widen or close the pupil. This allows the eye to take in more or less light depending on how bright it is around you. If it is too bright, the iris will shrink the pupil so that they eye can focus more effectively.<br/><br/>
							<b>Lacrimal Glands</b> - These glands are located on the outer corner of each eye. They produce tears which help moisten the eye when it becomes dry, and flush out particles which irritate the eye. As tears flush out potentially dangerous irritants, it becomes easier to focus properly.<br/><br/>
							<b>Lens</b> - The lens sits directly behind the pupil. This is a clear layer that focuses the light the pupil takes in. It is held in place by the ciliary muscles, which allow the lens to change shape depending on the amount of light that hits it so it can be properly focused.<br/><br/>
							<b>Pupil</b> - The pupil appears as a black dot in the middle of the eye. This black area is actually a hole that takes in light so the eye can focus on the objects in front of it.<br/><br/>
							<b>Retina</b> - The light focuses by the lens will be transmitted onto the retina. This is made of rods and cones arranged in layers, which will transmit light into chemicals and electrical pulses. The retina is located in the back of the eye, and is connected to the optic nerves that will transmit the images the eye sees to the brain so they can be interpreted. The back of the retina, known as the macula, will help interpret the details of the object the eye is working to interpret. The center of the macula, known as the fova will increase the detail of these images to a perceivable point.<br/><br/>
							<b>Sclera</b> - The sclera is commonly referred to as the "whites" of the eye. This is a smooth, white layer on the outside, but the inside is brown and contains grooves that help the tendons of the eye attach properly. The sclera provides structure and safety for the inner workings of the eye, but is also flexible so that the eye can move to seek out objects as necessary.<br/><br/>
							<b>Vitreous Humor</b> – The vitreous humor is the gel located in the back of the eye which helps it hold its shape. This gel takes in nutrients from the ciliary body, aqueous humor and the retinal vessels so the eye can remain healthy. When debris finds its way into the vitreous humor, it causes the eye to perceive "floaters," or spots that move across the vision area that cannot be attributed to objects in the environment.<br/><br/>
