							<b>Anvil</b> - (also called the incus) a tiny bone that passes vibrations from the hammer to the stirrup.<br/><br/>
							<b>Cochlea</b> - a spiral-shaped, fluid-filled inner ear structure; it is lined with cilia (tiny hairs) that move when vibrated and cause a nerve impulse to form.<br/><br/>
							<b>Eardrum</b> - (also called the tympanic membrane) a thin membrane that vibrates when sound waves reach it.<br/><br/>
							<b>Eustachian Tube</b> - a tube that connects the middle ear to the back of the nose; it equalizes the pressure between the middle ear and the air outside. When you "pop" your ears as you change altitude (going up a mountain or in an airplane), you are equalizing the air pressure in your middle ear.<br/><br/>
							<b>Hammer</b> - (also called the malleus) a tiny bone that passes vibrations from the eardrum to the anvil.<br/><br/>
							<b>Nerves</b> - these carry electro-chemical signals from the inner ear (the cochlea) to the brain.<br/><br/>
							<b>Outer Ear Canal</b> - the tube through which sound travels to the eardrum.<br/><br/>
							<b>Pinna</b> - (also called the auricle) the visible part of the outer ear. It collects sound and directs it into the outer ear canal<br/><br/>
							<b>Semicircular Canals</b> - three loops of fluid-filled tubes that are attached to the cochlea in the inner ear. They help us maintain our sense of balance.<br/><br/>
							<b>Stirrup</b> - (also called the stapes) a tiny, U-shaped bone that passes vibrations from the stirrup to the cochlea. This is the smallest bone in the human body (it is 0.25 to 0.33 cm long).<br/><br/>
