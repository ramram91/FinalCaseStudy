							<b>Ureter</b> is the tube that carries urine from the kidney to the urinary bladder. In humans there are two ureters, one attached to each kidney. The upper half of the ureter is located in the abdomen and the lower half is located in the pelvic area.  The ureter is approximately 12 inches long. The tube has thick walls composed of a fibrous, a muscular, and a mucus coat, which are able to contract.  Ureter disorders include: Duplication of the ureter: a congenital condition in which two ureters form on the same kidney. Ureteropelvic Junction obstruction: this occurs when the connection between the kidney and ureter is blocked, preventing urine from exiting the kidney. Ureterovesical Junction obstruction: When the connection between the ureter and bladder is blocked. If any of these disorders occur, the passage of urine is blocked and can cause pyelonephritis, loss of renal function, or renal calculi. Treatment is possible through insertion of a catheter, insertion of a stent, or surgery. If an infection is found, antibiotics are often prescribed.
