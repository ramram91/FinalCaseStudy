							To prevent leakage, circular muscles called <b>sphincters</b> close tightly around the opening of the bladder into the urethra, the tube that allows urine to pass outside the body. The only difference between the female and male urinary system is the length of the urethra. In females, the urethra is about 1.5 inches (3.8 cm) to 2 inches (5.1 cm) long and sits between the clitoris and the vagina. In males, it runs the length of the penis, is about 8 inches (20 cm) long and opens at the end of the penis. The male urethra is used to eliminate urine as well as semen during ejaculation.
							Nerves in the bladder send signals when it needs to be emptied. The sensation to urinate becomes stronger as the bladder reaches its limit. At that point, nerves from the bladder send a message to the brain that the bladder is full, and your urge to empty your bladder intensifies. When you urinate, the brain signals the bladder muscles to tighten while simultaneously signaling the sphincter muscles to relax.<br/><br/>	
						
							The term urethral sphincter refers to one of two muscles used to control the exit of urine in the urinary bladder through the urethra. The two muscles are the external urethral sphincter and the internal urethral sphincter. When either of these muscles contracts, the urethra is sealed shut.<br/><br/>

							The external urethral sphincter originates at the ischiopubic ramus and inserts into the intermeshing muscle fibers from the other side. It is controlled by the deep perineal branch of the pudendal nerve. Activity in the nerve fibers constricts the urethra.<br/><br/>

						•	The internal sphincter muscle of urethra: located at the bladder's inferior end and the urethra's proximal end at the junction of the urethra with the urinary bladder. The internal sphincter is a continuation of the detrusor muscle and is made of smooth muscle, therefore it is under involuntary or autonomic control. This is the primary muscle for prohibiting the release of urine.<br/><br/>
						•	The external sphincter muscle of urethra (sphincter urethrae): located at the bladder's distal inferior end in females and inferior to the prostate (at the level of the membranous urethra) in males is a secondary sphincter to control the flow of urine through the urethra. Unlike the internal sphincter muscle, the external sphincter is made of skeletal muscle, therefore it is under voluntary control of the somatic nervous system.

