							The bones of the pelvis are the hip bones, sacrum, and coccyx. Each hip bone contains three bones — the ilium, ischium, and pubis — that fuse together as we grow older. The base of the spine, a single bone consisting of five fused bones, is wedged between the illim.  It is known as the sacrum. The pelvis forms the base of the spine as well as the socket of the hip joint.
							
