					<div class="modal-body">
						<p>
							<b>Ventral Root</b> is a type of nerve root that branches off the spinal cord, exits the spinal column and continues on its path to specific areas of the body.<br/>

							At each level of the spinal cord, two nerve roots branch out, one to the left and one to the right. At the innermost base of each spinal nerve there are two roots, the ventral root (also called anterior root), and the dorsal root (also called the posterior root). <br/><br/>
							The ventral root is an efferent root, meaning it carries neural signals away from the central nervous system (the brain and spinal cord) and toward the limbs and organs. The dorsal root is an afferent root, meaning it carries neural signals from the limbs and organs back to the spinal cord and brain.<br/><br/>

							The dorsal root contains sensory fibers that control information like pain and temperature sensations, while the ventral root is made up of motor fibers that send electrochemical signals to the body’s muscles and glands.<br/><br/>
							<b>These impulses control the following:</b><br/>

							- Muscular contractions<br/>
							- Hormone synthesis<br/>
							- Gland secretion<br/><br/>
							There are several conditions that can lead to damage or constriction of a ventral root as it exits the spinal column. One example is foraminal stenosis, where the foramen (empty space between each vertebrae through which spinal nerves travel on their way to other parts of the body) begin to constrict or narrow. This constriction then puts undue stress on nerves passing through them, causing pain, numbness, tingling or, in acute cases, loss of some muscle functionality. Conditions that could cause foraminal stenosis include osteoarthirits, bone spurs, herniated discs or bulging discs.<br/><br/>

							If your physician has diagnosed you with a condition involving constriction of the ventral root, he or she may recommend physical therapy, chiropractic treatment, anti-inflammatory medication or steroidal injections. If these temporary treatments do not prove effective, contact Laser Spine Institute about our minimally invasive procedures that can help you find relief from pain. Laser Spine Institute will be happy to discuss your spinal condition and review your MRI or CT scan, completely free of charge.
