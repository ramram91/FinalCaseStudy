						<p>
							Thirty-one pairs of nerves exit the spinal cord through openings in the vertebral column. These are spinal nerves. There are four main areas of the vertebral spine: the cervical spine, or neck; the thoracic spine, or chest; the lumbar spine, or lower back; and the sacrum, or tailbone. Below the sacrum, a small bone called the coccyx marks the end of the spinal column. The nerves that exit the spinal cord are named according to their corresponding region of the vertebral spine.<br/><br/>
							<h2>Sensory Function</h2>
							The posterior, or dorsal, roots of spinal nerves carry sensory information from the areas they serve back to the brain. Spine-Health explains that the area of skin from which a particular nerve receives sensory information is known as that nerve's "dermatome." 
							<b>For example</b>, the skin over the shoulders sends sensory information to the fourth and fifth cervical nerves, also known as C4 and C5. The C7 nerve detects sensation in the middle finger. The little toes send sensory information to the first sacral nerve. Pain or numbness specific to a dermatome could be a symptom of damage to its corresponding nerves.
