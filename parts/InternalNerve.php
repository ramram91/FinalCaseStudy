					</div>
					<div class="modal-body">
						<p>
							<b>Dorsal Nerve Root</b>– also known as a posterior nerve root – is a main bundle of nerve fibers that branches off the spinal cord and goes to other areas of the body. Dorsal nerve roots are responsible for carrying signals of sensation (such as feelings of pain and temperature) from the body to the brain. The brain then processes these signals immediately, making you aware of the sensation. <br/>
							A pair of spinal nerves branches out from each level of the spinal cord, one to the left and one to the right. Naturally, a left spinal nerve serves the left side of the body and a right spinal nerve serves the right side of the body. There are 31 total pairs of spinal nerve roots that branch off the spinal cord, each one carrying a dorsal nerve root and a ventral nerve root.<br/><br/>

							At the innermost base of each spinal nerve root, therefore, there are two sub-roots—the dorsal nerve root and the ventral nerve root—that connect the nerve to the white and gray matter of the spinal cord. The dorsal nerve root receives sensory, or touch, signals from the body and carries these signals to the brain, while the ventral nerve root sends out signals of movement from the brain to the body.<br/><br/>

							Technically speaking, then, the dorsal nerve root is an afferent root, meaning it carries neural signals from the peripheral nervous system (PNS) to the central nervous system (CNS). The ventral rootis an efferent root, meaning it carries signals from the central nervous system (CNS) to the peripheral nervous system (PNS).<br/><br/>
							<b>Some important points to keep in mind about the dorsal nerve root include:</b><br/> <br/>

							- It controls pain and temperature sensations. <br/>
							- If severed, pinched or constricted, the signals of a dorsal nerve root will be intensified or interrupted. This can cause pain, numbness, a “pins and needles” sensation or the sensation of heat along the affected nerve. <br/>
							- It is extremely vulnerable in the foramen, or open space of each vertebra where nerve roots travel between the spinal cord and the rest of the body. <br/><br/>
							
							The foramen is the passageway for the nerves, so any disc damage or bone spurs that constrict this passageway can directly affect the nerve. <br/>
						</p>
