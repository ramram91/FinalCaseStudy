						The lymphatic system is a component of the immune system and works closely
						with the cardiovascular system. The lymphatic system is a vascular network of tubules and ducts that collect, filter, and return lymph to blood circulation. Lymph is a clear fluid that comes from blood plasma, which exits blood vessels at capillary beds. This fluid becomes the interstitial fluid that bathes tissues and helps to deliver nutrients and oxygen to cells . In addition to returning lymph to circulation, lymphatic structures also filter blood of microorganisms, such as bacteria and viruses . Lymphatic structures also remove cellular debris, cancerous cells , and waste from the blood. Once filtered, the blood is returned to the circulatory system. 

