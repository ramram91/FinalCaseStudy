						<p>
							<b>Trachea</b>, commonly known as the windpipe, is a tube about 4 inches long and less than an inch in diameter in most people. The trachea begins just under the larynx (voice box) and runs down behind the breastbone (sternum). The trachea then divides into two smaller tubes called bronchi: one bronchus for each lung. <br/><br/>
							The trachea is composed of about 20 rings of tough cartilage. The back part of each ring is made of muscle and connective tissue. Moist, smooth tissue called mucosa lines the inside of the trachea. The trachea widens and lengthens slightly with each breath in, returning to its resting size with each breath out. <br/><br/>
							<h2>Trachea Conditions</h2><br/>
							•	<b>Tracheal stenosis:</b> Inflammation in the trachea can lead to scarring and narrowing of the windpipe. Surgery or endoscopy may be needed to correct the narrowing (stenosis), if severe. <br/>
							•	<b>Tracheoesophageal fistula:</b> An abnormal channel forms to connect the trachea and the esophagus. Passage of swallowed food from the esophagus into the trachea causes serious lung problems. <br/>
							•	<b>Tracheal foreign body:</b> An object is inhaled (aspirated) and lodges in the trachea or one of its branches. A procedure called bronchoscopy is usually needed to remove a foreign body from the trachea. <br/>
							•	<b>Tracheal cancer:</b> Cancer of the trachea is quite rare. Symptoms can include coughing or difficulty breathing. <br/>
							•	<b>Tracheomalacia:</b> The trachea is soft and floppy rather than rigid, usually due to a birth defect. In adults, tracheomalacia is generally caused by injury or by smoking. <br/>
							•	<b>Tracheal obstruction:</b> A tumor or other growth can compress and narrow the trachea, causing difficulty breathing. A stent or surgery is needed to open the trachea and improve breathing. <br/>
							<h2> Trachea Tests </h2><br/>
							•	<b>Flexible bronchoscopy:</b> An endoscope (flexible tube with a lighted camera on its end) is passed through the nose or mouth into the trachea. Using bronchoscopy, a doctor can examine the trachea and its branches. <br/>
							•	<b>Rigid bronchoscopy:</b> A rigid metal tube is introduced through the mouth into the trachea. Rigid bronchoscopy is often more effective than flexible bronchoscopy, but it requires deep anesthesia. <br/>
							•	<b>Computed tomography ( CT scan):</b> A CT scanner takes a series of X-rays, and a computer creates detailed images of the trachea and nearby structures. <br/>
							•	<b>Magnetic resonance imaging (MRI scan):</b> An MRI scanner uses radio waves in a magnetic field to create images of the trachea and nearby structures. <br/>
							•	<b>Chest X-ray:</b> A plain X-ray can tell if the trachea is deviated to either side of the chest. An X-ray might also identify masses or foreign bodies. <br/>
							<h2>Trachea Treatments</h2><br/>
							•	<b>Tracheostomy:</b> A small hole is cut in the front of the trachea, through an incision in the neck. Tracheostomy is usually done for people who need a long period of mechanical ventilation (breathing support). <br/>
							•	<b>Tracheal dilation:</b> During bronchoscopy, a balloon can be inflated in the trachea, opening a narrowing (stenosis). Sequentially larger rings can also be used to gradually open the trachea. <br/>
							•	<b>Laser therapy:</b> Blockages in the trachea (such as from cancer) can be destroyed with a high-energy laser. <br/>
							•	<b>Tracheal stenting:</b> After dilation of a tracheal obstruction, a stent is often placed to keep the trachea open. Silicone or metal stents may be used. <br/>
							•	<b>Tracheal surgery:</b> Surgery may be best for removing certain tumors obstructing the trachea. Surgery may also correct a tracheoesophageal fistula. <br/>
							•	<b>Cryotherapy:</b> During bronchoscopy, a tool can freeze and destroy a tumor obstructing the trachea.


						</p>
