						<p>
							<b>Larynx</b> (voice box) is part of the respiratory system that holds the vocal cords. It is responsible for producing voice, helping us swallow and breathe. Air passes in and out of the larynx each time the body inhales or exhales. Air from the lungs passes over the stretched vocal cords, and the vibrations are modified by the tongue, palate, and lips to produce speech. <br/><br/>
								It is composed primarily of muscles and cartilages that are bound together by elastic tissues. It lies between the pharynx (upper part of the air passages) and the trachea (windpipe), and forms part of a tube in the throat that carries air to and from the lungs. It consists of areas of tough, flexible tissue called cartilage, which sticks out at the front of the throat to form the Adam's apple. Below this, connecting the thyroid cartilage to the trachea is another cartilage that is shaped like a signet ring with the seal at the back of it. Just on top of this seal are two pyramid-shaped cartilages, and between these two cartilages and the inner surface of the Adam's apple stretch two fibrous sheets of tissue, called the vocal cords, which are responsible for voice production. <br/><br/>
								The larynx also has the important function of preventing choking. When we are not eating or drinking, the epiglottis stays upright, keeping the larynx open as part of the airway to the lungs; as soon as swallowing begins, the epiglottis drops like a lid over the larynx, directing food to either side. Closing the vocal cords also helps to protect the air passage. The food or drink passes down the esophagus to the stomach. The larynx is used when we swallow, talk, and breathe.
