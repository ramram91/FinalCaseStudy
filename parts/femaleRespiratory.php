							As we head into the lungs, the trachea branches into two main sections, each called a <b>bronchus</b>. There is a right primary bronchus that goes into the right lung, and a left primary bronchus that goes into the left lung. Each of these bronchi (plural for bronchus) then branch into more bronchi. Those, in turn, branch into smaller tubes called bronchioles. All of this branching eventually results in a structure that truly resembles an upside-down tree. The trachea is the trunk, with all of the branches coming from it. For that reason, it is known as the bronchial tree.<br/><br/>
							Although this branching does not continue forever, it does happen about 25 times after the first branching of the trachea. The last bronchioles divide into what are called respiratory bronchioles, each of which divide into tiny openings called alveolar ducts. You can imagine how each tube has gotten smaller and smaller as it has branched. By the time we reach the alveoli, the tubes are microscopic - and there are millions of them!
