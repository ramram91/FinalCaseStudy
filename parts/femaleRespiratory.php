						<p>
							<b> Pharynx</b>, ( Greek: “throat”) cone-shaped passageway leading from the oral and nasal cavities in the head to the esophagus and larynx. The pharynx chamber serves both respiratory and digestive functions. Thick fibres of muscle and connective tissue attach the pharynx to the base of the skull and surrounding structures. Both circular and longitudinal muscles occur in the walls of this organ; the circular muscles form constrictions that help push food to the esophagus and prevent air from being swallowed, while the longitudinal fibres lift the walls of the pharynx during swallowing.<br/><br/>
								The pharynx consists of three main divisions. The anterior portion is the nasal pharynx, the back section of the nasal cavity. The nasal pharynx connects to the second region, the oral pharynx, by means of a passage called an isthmus. The oral pharynx begins at the back of the mouthcavity and continues down the throat to the epiglottis, a flap of tissue that covers the air passage to the lungs and that channels food to the esophagus. Triangular-shaped recesses in the walls of this region house the palatine tonsils (see tonsils), two masses of lymphatic tissue prone to infection. <br/><br/>
								The isthmus connecting the oral and nasal regions is extremely beneficial in humans. It allows them to breathe through either the nose or the mouth and, when medically necessary, allows food to be passed to the esophagus by nasal tubes. The third region is thelaryngeal pharynx, which begins at the epiglottis and leads down to the esophagus. Its function is to regulate the passage of air to the lungs and food to the esophagus. <br/><br/>
								Two small tubes (eustachian tubes) connect the middle ears to the pharynx and allow air pressure on the eardrum to be equalized. Head colds sometimes inflame these tubes, causing earaches and hearing difficulties. Other medical afflictions associated with the pharynx include tonsillitis, cancer, and various types of throat paralyses caused by polio, diphtheria, rabies, or nervous-system injuries
