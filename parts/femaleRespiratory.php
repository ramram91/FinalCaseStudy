						<p>							
							<b>Alveoli</b> have an elastic-like structure, which means that there is an amount of effort needed to inhale oxygen into the lungs, but that same structure assists with exhaling. The process of breathing is also made easier by a liquid that coats the alveoli and reduces the surface tension on the structures. This fluid is called surfactant, which reduces the surface tension by a factor of approximately 15. This allows the alveoli to inflate with a very small change in pressure. <br/><br/>
							<h2>Alveoli In Our Anatomy</h2><br/>
							At the end of each of the many tiny branches of our bronchial tree, we find openings to microscopic sacs. Each little sac is an alveolus, singular for alveoli. There may be several alveoli coming from one duct, forming a little clump. These groups of alveoli somewhat resemble a cluster of grapes that are all attached. It is in the alveoli that one of the most important transfers in our entire body takes place. It is here that the respiratory system comes into direct contact with the circulatory system, or blood vessels. <br/>
							<h2>Function Of Alveoli</h2><br/>
							We breathe in oxygen so that parts of our body can use it for many different cellular functions. But we must somehow get the oxygen from our lungs into our bloodstream, so it can be transported to the many places it is needed. Likewise, our bodies produce carbon dioxide as a waste product. It must be exhaled from our body, but must get from the bloodstream to our lungs. It is the alveoli that are responsible for this transfer of gases. 
