						<p>
						<b>Llio-tibial band</b> is classified as a deep fascia of the body, surrounding and connecting the muscles of the body to surrounding tissues. Like all other deep fascia, it is made almost exclusively of dense regular connective tissue. Dense regular connective tissue is a form of fibrous connective tissue that is extremely strong, tough, and avascular. It is made almost exclusively of collagen fibers and fibroblast cells, which produce collagen. Collagen is the strongest protein found in nature and is one of the strongest structures in the entire human body. The collagen fibers are arranged in a regular pattern of straight lines, giving the iliotibial tract incredible strength in the direction in which muscle force is applied to it and considerably less strength in other directions. A small number of elastin protein fibers are also found intermingled with the collagen fibers to permit a degree of elasticity in the tissue. Functionally, the iliotibial tract extends the tensor fascia latae muscle into the lower thigh and leg, allowing it to function as an abductor, medial rotator and flexor of the thigh. It also allows the tensor fascia latae and gluteus maximus muscles to support the extension of the knee while standing, walking, running and biking. A common injury to the iliotibial tract is iliotibial band syndrome (ITBS), a condition caused by the friction of the tract moving across the tissues on the lateral side of the thigh. When the knee flexes, the iliotibial band moves posteriorly over the bony ridge of the lateral condyle of the femur. It then passes over the lateral condyle again when it moves anteriorly during knee extension. The repeated flexion and extension involved in long distance running results in the iliotibial band becoming inflamed, irritated and painful. Fortunately, this condition is easily treated with rest, ice, compression and elevation (RICE).
