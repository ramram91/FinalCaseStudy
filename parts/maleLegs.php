						<b>Tibialis Anterior</b> is the largest muscle located in the anterior compartment of the human leg. The blood supply to the tibialis anterior muscle comes largely from the anterior tibial artery and its branches. In general, muscles of this compartment dorsiflex the foot at the ankle and extend the toes. The tibialis anterior muscle emerges from the lateral condyle, superior half of the lateral surface of the tibia, and from the interosseous membrane. The muscle inserts into the base of the first metatarsal. It also inserts to the medial and inferior surfaces of the medial cuneiform tarsal. Anterior compartment syndrome occurs from excessive contraction of anterior compartment muscles. Those who suffer this syndrome experience pain that radiates down the ankle and dorsum of the foot, overlying the extensor tendons of these muscles. The tibialis anterior muscle is responsible for the movements of the human leg. It also inverts the foot at the subtalar and midtarsal joints. It holds up the medial longitudinal arch of the foot.
