						<b>Patella (kneecap)</b> is commonly referred to as the kneecap. It is a small, independent bone that rests between the femur and tibia. The femur, also known as the thigh bone, has a dedicated groove that the kneecap slides along. As a form of protection, both bones also feature cartilage that interacts with the patella. The kneecap plays a vital role in how the knee bends, and as a result, most motions that require moving the leg. If it or the tendon associated with it becomes injured, a person will experience difficulty walking, running, standing, or engaging in athletic activity. If dislocated, the kneecap can no longer slide along the thigh bone's grooves, and this can aggravate and damage cartilage on both the femur and the tibia. Dislocation and other troubles are common among athletes and other people who are extremely physically active. Trouble may be even more pronounced in high impact sports. For example, patella-related injuries are common in sports like football, mixed martial arts, and wrestling.
