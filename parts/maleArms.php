							<b><font color="red">Biceps brachii</font></b>- This is probably the most often drawn muscle. It has two heads that run from different parts of the shoulder blade to the radius, and is one of the muscles that bend the arm, and turn the hand outward.<br/><br/>
							<b><font color="lightGreen"> Brachialis </font></b> - This is another elbow flexor. It lies below the biceps. Since it connects to the ulna, it doesn't twist the hand around.<br/><br/>
							<b><font color = "cyan"> Triceps </font></b> - This is the three-headed muscle on the back of the upper arm. Its job is to straighten the arm. It is only defined in more athletic individuals, and looks a bit like an upside down 'U'.<br/><br/>
							<b><font color = "blue"> Forearm </font> <font color = "violet"> muscles </font></b> - The two largest muscles of the forearm are indicated in the illustration. The brachioradialis helps the biceps to bend the arm, and will be most visible when the arm is bent 90 degrees. The extensor carpi radialis flexes the wrist. It is less pronounced than the brachioradialis, but contributes to the arm's contour when the hand is turned with the palm facing up.<br/><br/>
