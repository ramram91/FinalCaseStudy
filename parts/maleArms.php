							<br/>
							The hands are a notorious source of frustration. This section won't concentrate on the muscle structure, since the hand is very complex in this regard, and knowing this won't help much in drawing them. Instead, we'll look at proportions, range of motion, and possible simplifications.<br/><br/>
						
							<ul>
								<b>Bones</b> are hard tissues that give your hand shape and stability.<br/>
								<b>Phalanges</b> are the finger bones.<br/>
								<b>Metacarpals</b> are the hand bones.<br/>
								<b>Carpals</b> are the wrist bones.<br/>
								<b>Joints</b> are places where bones fit together, allowing movement.<br/>
								<b>Ligaments</b> are soft tissues that connect bone to bone and stabilize your joints.<br/>
								<b>Muscles</b> are soft tissues that contract (tighten) and relax to move your hand.<br/>
								<b>Synovial Lining</b> produces the fluid inside your joints that makes movement smooth.<br/>
								<b>Volar plates</b> are hard tissues that stabilize the joints, keeping fingers from bending backward.<br/>
								<b>Tendon sheaths</b> are fluid-filled tubes that surround, protect, and guide the tendons.<br/>
								<b>Tendons</b> are cordlike soft tissues that connect muscle to bone.<br/>
								<b>Blood vessels</b> carry blood to and from your hand.<br/>
								<b>Nerves</b> send and receive messages, allowing you to feel and helping to coordinate movement.<br/>
								<b>Palmar Fascia</b> is a firm layer of soft tissue that stabilizes the palm of your hand.<br/>
							</ul>
						
